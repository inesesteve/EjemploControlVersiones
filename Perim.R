AreaCuadrado<-function(L){
  return(4*L)
}

AreaTriangulo<-function(b,a){
  return((b*a)/2)
}

AreaRectangulo<-function(L,l){
  return(L*l)
}

AreaTrapecio<-function(b,B,H){
  return(((b+B)*H)/2)
}

PerimetroCuadrado<-function(L){
  return(4*L)
}

PerimetroCircum<-function(r){
  return(2*3.14159*r)
}

PerimetroRectangulo<-function(L,l){
  return((L+l)*2)
}
